#1. Absolute Value
print(abs(-10))


#2. Quotient and Remainder
print(divmod(10, 3))


#3. Maximum of Three Numbers
print(max(10, 25, 15))


#4. Minimum of Three Numbers
print(min(10, 25, 15))


#5. Length of an Object
print(len([1, 2, 3, 4, 5]))


#6. Power Function
print(pow(2, 3))


#7. Round a Number
print(round(4.6))


#8. Sum of Values
print(sum([1, 2, 3, 4, 5]))


#9. Type of a Value
print(type(10))


#10. Convert to Octal
print(oct(10))

